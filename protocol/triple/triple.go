const (
	// TRIPLE protocol name
	TRIPLE = "tri"
)

var (
	tripleProtocol *TripleProtocol
)

var tripleServerGracefulStop = func(server *Server) {
	server.GracefulStop()
}

func init() {
	extension.SetProtocol(TRIPLE, GetProtocol)

	// register graceful shutdown callback
	extension.RegisterGracefulShutdownCallback(TRIPLE, func(ctx context.Context) error {
		p := GetProtocol()
		if p == nil {
			return nil
		}

		tp, ok := p.(*TripleProtocol)
		if !ok {
			return nil
		}

		tp.ExporterMap().Range(func(key, value any) bool {
			serviceKey, ok := key.(string)
			if !ok || serviceKey == "" {
				return true
			}
			internal.HealthSetServingStatusNotServing(serviceKey)
			return true
		})

		return nil
	})
}

type TripleProtocol struct {
	base.BaseProtocol
	serverLock sync.Mutex
	serverMap  map[string]*Server
}

// Export TRIPLE service for remote invocation
func (tp *TripleProtocol) Export(invoker base.Invoker) base.Exporter {
	url := invoker.GetURL()
	serviceKey := url.ServiceKey()
	var info *common.ServiceInfo
	infoRaw, ok := url.GetAttribute(constant.ServiceInfoKey)
	if ok {
		info = infoRaw.(*common.ServiceInfo)
	}
	exporter := NewTripleExporter(serviceKey, invoker, tp.ExporterMap())
	tp.SetExporterMap(serviceKey, exporter)
	logger.Infof("[TRIPLE Protocol] Export service: %s", url.String())
	if err := tp.openServer(invoker, info); err != nil {
		exporter.UnExport()
		panic(err)
	}
	internal.HealthSetServingStatusServing(url.Service())
	return exporter
}

func (tp *TripleProtocol) openServer(invoker base.Invoker, info *common.ServiceInfo) error {
	url := invoker.GetURL()
	tp.serverLock.Lock()
	defer tp.serverLock.Unlock()

	if srv, ok := tp.serverMap[url.Location]; ok {
		if err := srv.ValidateTransportURL(url); err != nil {
			return err
		}
		if err := srv.refreshService(invoker, info); err != nil {
			return err
		}
		return nil
	}

	if _, ok := tp.ExporterMap().Load(url.ServiceKey()); !ok {
		panic("[TRIPLE Protocol]" + url.Key() + "is not existing")
	}

	// Do not freeze listener configuration at construction time. The shared
	// transport must be resolved from the current export URL so mount-first and
	// export-first flows converge on the same bootstrap path.
	srv := NewServer(nil)
	if err := srv.Start(invoker, info); err != nil {
		return err
	}
	tp.serverMap[url.Location] = srv
	return nil
}

// Refer a remote triple service
func (tp *TripleProtocol) Refer(url *common.URL) base.Invoker {
	var invoker base.Invoker
	var err error

	IDLMode := url.GetParam(constant.IDLMode, "")
	// for now, we do not need to use this info
	_, ok := url.GetAttribute(constant.ClientInfoKey)
	// Check if this is a generic call
	generic := url.GetParam(constant.GenericKey, "")
	isGenericCall := isGenericCall(generic)

	// Use NewTripleInvoker for:
	// 1. New protoc-gen-go-triple stub code (has ClientInfoKey)
	// 2. Non-IDL mode (IDLMode == NONIDL)
	// 3. Generic call (generic=true/gson/protobuf/protobuf-json)
	if ok || IDLMode == constant.NONIDL || isGenericCall {
		// new triple invoker supporting $invoke for generic calls
		invoker, err = NewTripleInvoker(url)
	} else {
		// stub code generated by old protoc-gen-go-triple
		invoker, err = NewDubbo3Invoker(url)
	}
	if err != nil {
		logger.Warnf("can't dial the server: %s", url.Key())
		return nil
	}

	tp.SetInvokers(invoker)
	logger.Infof("[TRIPLE Protocol] Refer service: %s", url.String())
	return invoker
}

func (tp *TripleProtocol) Destroy() {
	logger.Infof("TripleProtocol destroy.")

	for _, server := range tp.drainServers() {
		tripleServerGracefulStop(server)
	}

	tp.BaseProtocol.Destroy()
}

func (tp *TripleProtocol) drainServers() []*Server {
	tp.serverLock.Lock()
	defer tp.serverLock.Unlock()

	servers := make([]*Server, 0, len(tp.serverMap))
	for key, server := range tp.serverMap {
		delete(tp.serverMap, key)
		servers = append(servers, server)
	}
	return servers
}

// MountHTTPHandler attaches a root HTTP handler to the Triple listener at the
// given location. If the listener already exists, the handler is applied to the
// existing server; otherwise the Triple transport is started eagerly so later
// service export can reuse the same listener.
func (tp *TripleProtocol) MountHTTPHandler(url *common.URL, handler http.Handler) error {
	if url == nil {
		return fmt.Errorf("triple server url must not be nil")
	}
	if handler == nil {
		return fmt.Errorf("mounted HTTP handler must not be nil")
	}

	tp.serverLock.Lock()
	defer tp.serverLock.Unlock()

	if srv, ok := tp.serverMap[url.Location]; ok {
		if err := srv.ValidateTransportURL(url); err != nil {
			return err
		}
		// If service export already created the listener, mounting only needs to
		// attach the root handler to the existing server instance.
		srv.SetMountedHTTPHandler(handler)
		return nil
	}

	// Keep construction side-effect free and let StartHTTPTransport resolve
	// the actual listener settings from the mount URL.
	srv := NewServer(nil)
	srv.SetMountedHTTPHandler(handler)
	// Boot transport eagerly so HTTP-only and mount-before-export scenarios
	// reuse the same Triple listener once services are exported later.
	if err := srv.StartHTTPTransport(url); err != nil {
		return err
	}

	tp.serverMap[url.Location] = srv
	return nil
}

// isGenericCall checks if the generic parameter indicates a generic call
func isGenericCall(generic string) bool {
	if generic == "" {
		return false
	}
	return strings.EqualFold(generic, constant.GenericSerializationDefault) ||
		strings.EqualFold(generic, constant.GenericSerializationGson) ||
		strings.EqualFold(generic, constant.GenericSerializationProtobuf) ||
		strings.EqualFold(generic, constant.GenericSerializationProtobufJson)
}

func NewTripleProtocol() *TripleProtocol {
	return &TripleProtocol{
		BaseProtocol: base.NewBaseProtocol(),
		serverMap:    make(map[string]*Server),
	}
}

func GetProtocol() base.Protocol {
	if tripleProtocol == nil {
		tripleProtocol = NewTripleProtocol()
	}
	return tripleProtocol
}

type ServiceInfo struct {
	InterfaceName string
	ServiceType   any
	Methods       []MethodInfo
	Meta          map[string]any
}

type MethodInfo struct {
	Name           string
	Type           string
	ReqInitFunc    func() any
	StreamInitFunc func(baseStream any) any
	MethodFunc     func(ctx context.Context, args []any, handler any) (any, error)
	Meta           map[string]any
}
